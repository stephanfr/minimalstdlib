    TEST(TupleTests, TuplesCreatedWithReferences)
    {
        int a = 0, b = 1, c = 2;

        tuple t1 = {minstd::ref(a), minstd::ref(b)};
        tuple t2 = {minstd::ref(a), minstd::ref(c)};

        bool is_same = minstd::is_same_v<decltype(t1), tuple<int &, int &>>;

        CHECK(is_same);

        CHECK(t1 < t2);
        CHECK(t1 != t2);
        CHECK(!(t1 == t2));
        CHECK(!(t1 > t2));

        minstd::swap(b, c);

        CHECK(t2 < t1);
        CHECK(t1 != t2);
        CHECK(!(t1 == t2));
        CHECK(!(t2 > t1));
    }

