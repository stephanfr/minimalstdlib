    TEST(TupleTests, TuplesContainingRefs)
    {
        {
            int a = 0;
            int b = 0;

            minstd::fixed_string<> c;

            auto tup = tuple{minstd::ref(a), minstd::ref(b), minstd::ref(c)};

            static_assert(minstd::is_same_v<minstd::tuple_element<0, decltype(tup)>::type, int &>, "Expected int& at element 0");
            static_assert(minstd::is_same_v<minstd::tuple_element<1, decltype(tup)>::type, int &>, "Expected int& at element 1");
            static_assert(minstd::is_same_v<minstd::tuple_element<2, decltype(tup)>::type, minstd::fixed_string<> &>, "Expected minstd::string& at element 2");

            using minstd::get;

            tup = tuple{1, 2, "Hello, world!"};

            CHECK((a == 1 && b == 2 && c == "Hello, world!"));
        }

        {
            int a = 0;
            int b = 0;

            minstd::fixed_string<> c;

            CHECK((a == 0 && b == 0 && c == ""));

            auto tup = minstd::tie(a, b, c);

            static_assert(minstd::is_same_v<minstd::tuple_element<0, decltype(tup)>::type, int &>, "Expected int& at element 0");
            static_assert(minstd::is_same_v<minstd::tuple_element<1, decltype(tup)>::type, int &>, "Expected int& at element 1");
            static_assert(minstd::is_same_v<minstd::tuple_element<2, decltype(tup)>::type, minstd::fixed_string<> &>, "Expected minstd::string& at element 2");

            tup = tuple{1, 2, "Hello, world!"};

            CHECK((a == 1 && b == 2 && c == "Hello, world!"));
        }

        {
            int a = 0;
            int b = 0;

            minstd::fixed_string<> c;

            bool is_equal = minstd::tie(a, b, c) == minstd::tie(a, b, c);

            CHECK(is_equal);
        }

        {
            int a = 0;
            int b = 0;

            minstd::fixed_string<> c;

            bool is_equal = minstd::tie(a, b, c) == tuple{a, b, c};

            CHECK(is_equal);
        }
    }

