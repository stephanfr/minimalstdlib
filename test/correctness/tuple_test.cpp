        static_assert(!_sfinae_test_eq<decltype(t1), int>(0));
        static_assert(!_sfinae_test_less<decltype(t1), int>(0));
        static_assert(!_sfinae_test_eq<int, decltype(t1)>(0));
        static_assert(!_sfinae_test_less<int, decltype(t1)>(0));
        static_assert(!_sfinae_test_eq<decltype(t1), decltype(t2)>(0));
        static_assert(!_sfinae_test_less<decltype(t1), decltype(t2)>(0));
        static_assert(!_sfinae_test_eq<decltype(t1), decltype(t3)>(0));
        static_assert(!_sfinae_test_less<decltype(t1), decltype(t3)>(0));
        static_assert(!_sfinae_test_eq<decltype(t3), decltype(t2)>(0));
        static_assert(!_sfinae_test_less<decltype(t3), decltype(t2)>(0));

        auto t_short = tuple{1};
